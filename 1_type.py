# String (cadena de texto)
hola = "Hello world!"
hola = str("Hello world!")

# Integer (entero, sin decimales)
entero = 23
entero = int(23)

# Float (decimal)
decimal = 2.3
decimal = float(2.3)

# Complex (complejo)
complejo = 1 + 3j

# Bool (True/False)
estado = True

# Mostrar el tipo de variable
print(type(hola))
print(type(entero))
print(type(decimal))
print(type(complejo))
print(type(estado))
